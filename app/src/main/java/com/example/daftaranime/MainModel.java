    public int getGambarAnime() {
        return gambarAnime;
    }

    public void setGambarAnime(int gambarAnime) {
        this.gambarAnime = gambarAnime;
    }

    public String getGenreAnime() {
        return genreAnime;
    }

    public void setGenreAnime(String genreAnime) {
        this.genreAnime = genreAnime;
    }
